        <h2>Velkommen</h2>
        <p> Bergen er Norges nest største by og ligger på vestkysten av landet. Det er en koselig by med både historie, kultur og næringsliv. Her kan man gå på en fjelltur og se utover byen, eller sitte på en kafe på Bryggen og se utover Vågen. 
            På denne siden kan du få informasjon om noen aktiviteter, som fjell, restauranter og severdigheter, i Bergen og se værmeldingen for den kommende uken. Det er også mulig å lage en aktivitetsplan med aktiviteter og når du ønsker å gjennomføre de.
