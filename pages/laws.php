        <h2>Åndsverksloven</h2>
        <p> Lovens formål er å beskytte opphavsrettighetene til den som har laget produktet. Det vil si at man ikke kan bruke materialet fra en annen uten å vise hvor det kom fra eller referere til opphaveren. 
            F.eks ifølge §23 (eneretten til fotografiske bilder) så må man ha med navn på fotografen om man bruker fotografiske bilder. Ifølge §79 (straff) kan man bli straffet med bot og fengsel i inntil 1 år om man offentliggjør et bildet uten opphaverens navn eller samtykke. Dette gjelder også ved bruk av litterære og fakutlære 
            Reglenes virkeområde er norske statsborgere, folk som er bosatt i Norge og selskaper som enten har styre eller etablering i landet.
        </p> 

        <h2> Personopplysningsloven</h2>
        <p>Formålet med loven er å ivareta personvernet og sikkerheten til den enkelte. Loven skal også sørge for at personvernet og personopplysningene blir behandlet på en riktig og sikker måte. På den måten kan vi redusere sjansen for at opplysninger om f.eks privatliv skal havner på avveie
            Loven gjelder for alle personopplysninger som blir brukt i elektroniske hjelpemidler eller hvis de skal oppbevares i et personregister. Loven gjelder også bare innenfor den norske grensen.
            Et eksempel som kan være hensiktsmessig i forhold til nettsiden er §28. Den sier at det er forbudt å lagre personopplysninger som ikke blir brukt. Derfor har jeg i påloggingen ikke tatt med adresse eller personnummer fordi det er urelevant. 
        </p>

